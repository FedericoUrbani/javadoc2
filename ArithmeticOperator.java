/**
 * ArithmeticOperator class wich contains operations methods
 * @author federico
 */
public class ArithmeticOperator {
    /**
     * sum method
     * @param num1
     * @param num2
     * @return num1+num2
     * @author federico
     */
    public int sum(int num1, int num2){

        return num1+num2;
    }

    /**
     * sub method
     * @param num1
     * @param num2
     * @return num1-num2
     * @author federico
     */
    public int sub(int num1, int num2){

        return num1-num2;
    }

    /**
     * multiplication method
     * @param num1
     * @param num2
     * @return num1*num2
     * @author federico
     */
    public int mul(int num1, int num2){

        return num1*num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return num1/num2
     * @author federico
     */
    public int div(int num1, int num2){

        return num1/num2;
    }

}
